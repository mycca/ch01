public class Demo01 {

	public static void main(String[] args) {
		// 数据类型
		boolean b = true;
		char c = 'a';
		byte d = 10;
		short d1 = 10;
		int d2 = 10;
		long d3 = 10;
		float e = 10;
		double e1 = 10;
		// 自动类型转换
		int aa = 100;
		long bb = 1000;
		long cc = aa + bb;

		// 强制转换
		long dd = 1000000;
		long ee = 999999;
		int ff = (int) (dd + ee);
		// 算数运算符
		int aaa = 3;
		System.out.println(aaa++);// 先赋值再加
		System.out.println(aaa + 3);// 先赋值再加3
		System.out.println(+aaa);// 先加1再赋值
		System.out.println(++aaa);// 先加两个1再赋值

		int x = 2;// 赋值
		x += 2;// 先加再赋值结果等于4
		x -= 2;// 先减再赋值结果等于0
		x *= 2;// 先乘再赋值结果等于4
		x /= 2;// 先除再赋值结果等于1
		x %= 2;// 先取模再赋值结果等于0
		// 三元运算符
		int y=100;
		int z=1000;
		System.out.println((y>z)?"a":"b");//y大于z吗?是的返回a,不是返回b
		
		

	}

}
